
public class Info {
	private String name;
	private int phone;
	private int id;
	
	public String getName() {
		return name;
	}
	public void setName(String s) {
		name = s;
	}
	public int getPhone() {
		return phone;
	}
	public void setPhone(int num) {
		phone = num;
	}
	public int getId() {
		return id;
	}
	public void setId(int i) {
		id = i;
	}
}
